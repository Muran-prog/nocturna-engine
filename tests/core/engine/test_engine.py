async def test_dedup_last_finding_wins():
    """When fingerprints collide, the last finding (by insertion order) wins."""
    engine = NocturnaEngine()
    request = _make_request()

    # Build scan results with duplicate fingerprints
    target = request.targets[0].domain or "unknown"
    f1 = Finding(
        finding_id="first-id",
        title="Shared title",
        description="First occurrence.",
        severity=SeverityLevel.HIGH,
        tool="test_tool",
        target=target,
        cwe="CWE-79",
        evidence={"endpoint": "/a"},
    )
    f2 = Finding(
        finding_id="second-id",
        title="Shared title",
        description="Second occurrence should win.",
        severity=SeverityLevel.HIGH,
        tool="test_tool",
        target=target,
        cwe="CWE-79",
        evidence={"endpoint": "/a"},
    )
    assert f1.fingerprint == f2.fingerprint

    result = engine._deduplicate_findings_by_fingerprint(
        [f1, f2], stage="test", request_id="test-req"
    )
    assert len(result) == 1
    assert result[0].finding_id == "second-id"


