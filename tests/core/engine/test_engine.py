class MultiFindingTool(BaseTool):
    """Produces multiple findings with same fingerprint for dedup testing."""
    name: ClassVar[str] = "multi_finding_tool"

    async def execute(self, request: ScanRequest) -> ScanResult:
        target = request.targets[0].domain or str(request.targets[0].ip)
        return ScanResult(
            request_id=request.request_id,
            tool_name=self.name,
            raw_output={},
            findings=[
                Finding(
                    title="Duplicate finding",
                    description="First occurrence for dedup.",
                    severity=SeverityLevel.HIGH,
                    tool=self.name,
                    target=target or "unknown",
                    cwe="CWE-89",
                    evidence={"endpoint": "/api"},
                    metadata={"idx": "first"},
                ),
                Finding(
                    title="Duplicate finding",
                    description="Second occurrence for dedup.",
                    severity=SeverityLevel.HIGH,
                    tool=self.name,
                    target=target or "unknown",
                    cwe="CWE-89",
                    evidence={"endpoint": "/api"},
                    metadata={"idx": "second"},
                ),
                Finding(
                    title="Unique finding",
                    description="Unique finding no collision.",
                    severity=SeverityLevel.LOW,
                    tool=self.name,
                    target=target or "unknown",
                ),
            ],
        )

    async def parse_output(
        self,
        raw_output: dict[str, Any] | list[Any] | str | None,
        request: ScanRequest,
    ) -> list[Finding]:
        return []


